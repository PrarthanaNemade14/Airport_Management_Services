    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        String oldRoomName = hiddenNameField.getText();
        String newRoomName = txtName.getText();
        String price = txtPrice.getText();
        
        if(newRoomName == null || "".equals(newRoomName.trim()) || !Pattern.matches("^(?![\\s.]+$)[a-zA-Z\\s.]*$", newRoomName)) {
            showMessageDialog(this, "Please enter a valid room name", "Error", ERROR_MESSAGE);
            return;
        }
        else if(price == null || "".equals(price.trim()) 
                || !Pattern.matches("^[0-9]{1,3}$", price.trim())) {
            showMessageDialog(this, "Please enter a valid price", "Error", ERROR_MESSAGE);
            return;
        }
        
        hotel.editMenu(oldRoomName, newRoomName, price);
        JOptionPane.showMessageDialog(this, "Room edited successfully");
        txtName.setText("");
        txtPrice.setText("");
        populateTable();
    }//GEN-LAST:event_btnSaveActionPerformed


