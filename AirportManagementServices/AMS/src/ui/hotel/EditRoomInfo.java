    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        int selectedRow = tblRoomList.getSelectedRow();
        if(selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row to delete customer detail", "Error", ERROR_MESSAGE);
            return;
        }
        DefaultTableModel model = (DefaultTableModel) tblRoomList.getModel();
        String roomName = (String) model.getValueAt(selectedRow, 0);
        String price = (String) model.getValueAt(selectedRow, 1);
        hiddenNameField.setText(roomName);
        txtName.setText(roomName);
        txtPrice.setText(price);
        populateTable();
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int selectedRow = tblRoomList.getSelectedRow();
        if(selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row to delete room", "Error", ERROR_MESSAGE);
            return;
        }
        DefaultTableModel model = (DefaultTableModel) tblRoomList.getModel();
        String roomName = (String) model.getValueAt(selectedRow, 0);
        hotel.deleteMenu(roomName);
        JOptionPane.showMessageDialog(this, "Room deleted successfully");
        populateTable();
    }//GEN-LAST:event_btnDeleteActionPerformed

