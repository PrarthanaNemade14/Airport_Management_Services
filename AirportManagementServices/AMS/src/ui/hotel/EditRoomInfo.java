    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblRoomList.getModel();
        model.setRowCount(0);
        List<HotelRoom> menu = hotel.getMenu();

        for(HotelRoom room : menu)
        {
            Object[] row = new Object[2];
            row[0] = room.getName();
            row[1] = room.getPrice();
            model.addRow(row);
        }
    }
