    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String flightNo = txtFlightNo.getText();
        Airplane airplane = ecoSystem.getAirplaneDirectory().getAirplaneFromAirplaneNo(flightNo);
        String status = txtStatus.getText();
        String terminal = (String) jComboBoxTerminal.getSelectedItem();
        String gateNo = (String) jComboBoxGateNo.getSelectedItem();
        
        if(flightNo == null || "".equals(flightNo.trim()))
        {
            showMessageDialog(this, "Flight No cannot be empty", "Error", ERROR_MESSAGE);
            return;
        }
        else if(terminal == null)
        {
            showMessageDialog(this, "Please enter valid terminal", "Error", ERROR_MESSAGE);
            return;
        }
        else if(gateNo == null)
        {
            showMessageDialog(this, "Please enter valid gate no", "Error", ERROR_MESSAGE);
            return;
        }
        
        if(status.equals(Constant.arrival))
        {
            airplane.setArrivalTerminal(terminal);
            airplane.setArrivalGate(gateNo);
            airplane.setIsArrivalRequestPending(false);
        }
        else
        {
            airplane.setDepartureTerminal(terminal);
            airplane.setDepartureGate(gateNo);
            airplane.setIsDepartureRequestPending(false);
        }
        
        txtAirline.setText("");
        txtFlightNo.setText("");
        txtStatus.setText("");
        
        populateTable();
    }//GEN-LAST:event_jButton1ActionPerformed

