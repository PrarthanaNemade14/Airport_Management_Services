    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int selectedRow = tblResult.getSelectedRow();
        if(selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row to view booking detail", "Error", ERROR_MESSAGE);
            return;
        }
        DefaultTableModel model = (DefaultTableModel) tblResult.getModel();
        Airplane airplane = (Airplane) model.getValueAt(selectedRow, 1);
        String status = (String) model.getValueAt(selectedRow, 4);
        txtAirline.setText(airplane.getAirline().getName());
        txtFlightNo.setText(airplane.getAirplaneNo());
        txtStatus.setText(status);
    }//GEN-LAST:event_jButton2ActionPerformed


