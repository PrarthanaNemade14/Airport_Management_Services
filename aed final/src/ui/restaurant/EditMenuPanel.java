    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        int selectedRow = tblDishList.getSelectedRow();
        if(selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row to delete customer detail", "Error", ERROR_MESSAGE);
            return;
        }
        DefaultTableModel model = (DefaultTableModel) tblDishList.getModel();
        String dishName = (String) model.getValueAt(selectedRow, 0);
        String price = (String) model.getValueAt(selectedRow, 1);
        hiddenNameField.setText(dishName);
        txtName.setText(dishName);
        txtPrice.setText(price);
        populateTable();
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int selectedRow = tblDishList.getSelectedRow();
        if(selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row to delete dish", "Error", ERROR_MESSAGE);
            return;
        }
        DefaultTableModel model = (DefaultTableModel) tblDishList.getModel();
        String dishName = (String) model.getValueAt(selectedRow, 0);
        restaurant.deleteMenu(dishName);
        JOptionPane.showMessageDialog(this, "Dish deleted successfully");
        populateTable();
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        String oldDishName = hiddenNameField.getText();
        String newDishName = txtName.getText();
        String price = txtPrice.getText();
        
        if(newDishName == null || "".equals(newDishName.trim()) || !Pattern.matches("^(?![\\s.]+$)[a-zA-Z\\s.]*$", newDishName)) {
            showMessageDialog(this, "Please enter a valid dish name", "Error", ERROR_MESSAGE);
            return;
        }
        else if(price == null || "".equals(price.trim()) 
                || !Pattern.matches("^[0-9]{1,3}$", price.trim())) {
            showMessageDialog(this, "Please enter a valid price", "Error", ERROR_MESSAGE);
            return;
        }
        
        restaurant.editMenu(oldDishName, newDishName, price);
        JOptionPane.showMessageDialog(this, "Dish edited successfully");
        txtName.setText("");
        txtPrice.setText("");
        populateTable();
    }//GEN-LAST:event_btnSaveActionPerformed


