    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblDishList.getModel();
        model.setRowCount(0);
        List<Dish> menu = restaurant.getMenu();

        for(Dish dish : menu)
        {
            Object[] row = new Object[2];
            row[0] = dish.getName();
            row[1] = dish.getPrice();
            model.addRow(row);
        }
    }
